% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
temp = str2double(get(handles.edit1,'String'));
setappdata(0,'Ne_cap',temp);
temp = str2double(get(handles.edit2,'String'));
setappdata(0,'Vb',temp);
temp = str2double(get(handles.edit3,'String'));
setappdata(0,'Va',temp);
temp = str2double(get(handles.edit4,'String'));
setappdata(0,'epsr',temp);
temp = str2double(get(handles.edit5,'String'));
setappdata(0,'rhoo',temp);
temp = str2double(get(handles.edit6,'String'));
setappdata(0,'L_cap',temp);


L = getappdata(0,'L_cap');
rho0 = getappdata(0,'rhoo');
epsr = getappdata(0,'epsr');
Va = getappdata(0,'Va');
Vb = getappdata(0,'Vb');
Ne = getappdata(0,'Ne_cap');

[er1,er2,xeval,Veval,Vexact,xeval2,Eeval,Eexact]=fem_funct(L,rho0,epsr,Va,Vb,Ne)


set(handles.edit7,'String',er1);
set(handles.edit8,'String',er2);


%set the plots
axes(handles.axes1)
plot(xeval,Veval,'k--'); % Plot the Electric potential obtained from the finite element method
hold on;
plot(xeval,Vexact,'k-'); % Plot the Electric potential obtained from the exact analytical solution
xlabel('x (meters)');
ylabel('V (Volts)');
legend('FEM','Exact');
hold off;

axes(handles.axes2)
plot(xeval2,Eeval,'k--'); % Plot the Electric field obtained from the finite element method
hold on;
plot(xeval,Eexact,'k-'); % Plot the Electric field obtained from the exact analytical solution
xlabel('x (meters)');
ylabel('E (V/m)');
legend('FEM','Exact');
hold off;
